#include<stdio.h>
int main(){
    // print var type INT
    int integer_num = 10;
    printf("Integer: %d\n", integer_num);

    // print var type FLOAT
    float floating_point_num = 10.4f;
    printf("Float: %f\n", floating_point_num);

    // print var type DOUBLE
    double double_num = 2.34820283;
    printf("Double: %lf\n", double_num);

    // print var type CHAR
    char character = 'd';
    printf("Char: %c\n", character);
}